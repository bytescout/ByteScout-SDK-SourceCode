using System.Diagnostics;
using Bytescout.PDF;

namespace ThreeDAnnotationExample
{
    /// <summary>
    /// This example demonstrates how to create 3D annotation.
    /// </summary>
    class Program
    {
        static void Main()
        {
            // Create new document
            Document pdfDocument = new Document();
            pdfDocument.RegistrationName = "demo";
            pdfDocument.RegistrationKey = "demo";
            // Add page
            Page page = new Page(PaperFormat.A4);
            pdfDocument.Pages.Add(page);

            // Add 3D annotation from .u3d file
            ThreeDData model = new ThreeDData("box.u3d");
            ThreeDAnnotation annotation = new ThreeDAnnotation(model, 100, 100, 200, 200);
            page.Annotations.Add(annotation);

            // Save document to file
            pdfDocument.Save("result.pdf");

            // Cleanup 
            pdfDocument.Dispose();

            // Open result document in default associated application (for demo purpose)
            ProcessStartInfo processStartInfo = new ProcessStartInfo("result.pdf");
            processStartInfo.UseShellExecute = true;
            Process.Start(processStartInfo);
        }
    }
}
